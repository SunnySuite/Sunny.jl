Ir0c, Ir1a, Ir1b, Ir2, Ir3, Ir4, Ir5, Ir6, Mn0, Mn1, Mn2, Mn3, Mn4, Mn5, Mo0,
Mo1, Nb0, Nb1, Nd2, Nd3, Ni0, Ni1, Ni2, Ni3, Ni4, Np3, Np4, Np5, Np6, Os0a,
Os0b, Os0c, Os1a, Os1b, Os2, Os3, Os4, Os5, Os6, Os7, Pd0, Pd1, Pr3, Pt1, Pt2,
Pt3, Pt4, Pt5, Pt6, Pu3, Pu4, Pu5, Pu6, Re0a, Re0b, Re0c, Re1a, Re1b, Re2, Re3,
Re4, Re5, Re6, Rh0, Rh1, Ru0, Ru1, Sc0, Sc1, Sc2, Sm2, Sm3, Ta2, Ta3, Ta4, Tb2,
Tb3, Tc0, Tc1, Ti0, Ti1, Ti2, Ti3, Tm2, Tm3, U3, U4, U5, V0, V1, V2, V3, V4,
W0a, W0b, W0c, W1a, W1b, W2c, W3, W4, W5, Y0, Yb2, Yb3, Zr0, Zr1
```

The trailing number denotes ionization state. For example, `"Fe0"` denotes a
neutral iron atom, while `"Fe2"` denotes Fe¬≤‚Å∫. If multiple electronic
configurations are possible, they will be distinguished by a trailing letter
(`a`, `b`, ...). Omitting this letter will print an informative error,

```
FormFactor("Ir0")

ERROR: Disambiguate form factor according to electronic configuration:
    "Ir0a" -- 6s‚Å∞5d‚Åπ
    "Ir0b" -- 6s¬π5d‚Å∏
    "Ir0c" -- 6s¬≤5d‚Å∑
```

In the dipolar approximation (small ``|ùê™|``) the form factor is

``F(s) = ‚ü®j_0(s)‚ü© + [(2-g)/g] ‚ü®j_2(s)‚ü©``,

involving ``s = |ùê™|/4œÄ`` and the Land√© ``g``-factor. The ``‚ü®j_l(s)‚ü©`` are
radial averages of the ``l``th spherical Bessel function of the magnetic dipole.
More details are provided in Ref. [1].

The standard approximation tables involve expansion in Gaussians,

```math
‚ü®j_0(s)‚ü© = A e^{-as^2} + B e^{-bs^2} + C e^{-cs^2} + D e^{-ds^2} + E
```
and
```math
‚ü®j_2(s)‚ü© = (A e^{-as^2} + B e^{-bs^2} + C e^{-cs^2} + D e^{-ds^2} + E) s^2.
```

Sunny uses form factor tables as documented in the McPhase package [1]. For 3d
and 4d transition atoms and ions, rare-earth ions (4f), and actinide ions (5f),
the coefficients follow the Neutron Data Booklet [2]. The fit for Ce¬≥‚Å∫ follows
Lisher and Forsyth [3]. Additional 5d transition elements (Hf-Au) follow
Kobayashi, Nagao, and Ito [4].

Two special, ``ùê™``-independent form factor values are available:
`one(FormFactor)` and `zero(FormFactor)`. The first idealizes the magnetic ion
as a perfect point particle, while the second zeros all contributions from the
magnetic ion.

## References

1. Coefficient tables in [McPhase
   documentation](https://mcphase.github.io/webpage/manual/node148.html#ffacts).
2. [P. J. Brown, The Neutron Data Booklet, 2nd ed., Sec. 2.5 _Magnetic Form
   Factors_ (2003)](https://www.ill.eu/sites/ccsl/ffacts/ffachtml.html).
3. [Lisher and Forsyth, _Analytic approximations to form factors_, Acta Cryst. A
   **27**, 545-549 (1971)](https://doi.org/10.1107/S0567739471001219).
4. [K. Kobayashi, T. Nagao, M. Ito, _Radial integrals for the magnetic form
   factor of 5d transition elements_, Acta Cryst. A **67**, 473‚Äì480
   (2011)](https://doi.org/10.1107/S010876731102633X).
"""
function FormFactor(ion::String; g_lande=2)
    if !haskey(radial_integral_coefficients, ion)
        if !haskey(radial_integral_coefficients, ion*"a")
            error("Form factor requires species name and charge state, e.g. \"Fe2\" for Fe¬≤‚Å∫")
        else
            avail_keys = [k for k in keys(radial_integral_coefficients) if startswith(k, ion)]
            avail_strs = map(sort(avail_keys)) do k
                "    $(repr(k)) -- " * radial_integral_coefficients[k][3]
            end
            error("""
                Disambiguate form factor according to electronic configuration:
                $(join(avail_strs, "\n"))
                """)
        end
    end

    (j0, j2, config) = radial_integral_coefficients[ion]
    j0 = ExpandedBesselIntegral(j0...)
    j2 = ExpandedBesselIntegral(j2...)
    FormFactor(j0, j2, config, g_lande)
end

function Base.convert(::Type{FormFactor}, x::String)
    return FormFactor(x)
end

function compute_gaussian_expansion(j::ExpandedBesselIntegral, s2)
    (; A, a, B, b, C, c, D, d, E) = j
    return A*exp(-a*s2) + B*exp(-b*s2) + C*exp(-c*s2) + D*exp(-d*s2) + E
end

function compute_form_factor(form_factor::FormFactor, q2_absolute::Float64)
    (; j0, j2, g) = form_factor

    # Return early if this is the identity form factor
    (j0.A == j0.B == j0.C == j0.D == 0) && (j0.E == 1) && (g == 2) && return 1.0

    s2 = q2_absolute / (4œÄ)^2
    if g == 2
        return compute_gaussian_expansion(j0, s2)
    else
        J0 = compute_gaussian_expansion(j0, s2)
        J2 = compute_gaussian_expansion(j2, s2) * s2
        return J0 + ((2-g)/g) * J2
    end
end


