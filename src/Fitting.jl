function CRC.ProjectTo(sys::System)
    n = length(sys.active_labels)
    function project(őĒsys)
        őĒsys = CRC.unthunk(őĒsys)
        if őĒsys isa CRC.NoTangent || őĒsys isa CRC.AbstractZero
            return SystemTangent(zeros(n))
        elseif őĒsys isa SystemTangent
            return őĒsys
        else
            # if hasproperty(őĒsys, :vals)
            #     return SystemTangent(getproperty(őĒsys, :vals))
            # end
            error("Unsupported cotangent for System: $(typeof(őĒsys))")
        end
    end
    return project
end

