function CRC.ProjectTo(sys::System)
    n = length(sys.active_labels)
    function project(О”sys)
        О”sys = CRC.unthunk(О”sys)
        if О”sys isa CRC.NoTangent || О”sys isa CRC.AbstractZero
            return SystemTangent(zeros(n))
        elseif О”sys isa SystemTangent
            return О”sys
        else
            # if hasproperty(О”sys, :vals)
            #     return SystemTangent(getproperty(О”sys, :vals))
            # end
            error("Unsupported cotangent for System: $(typeof(О”sys))")
        end
    end
    return project
end

